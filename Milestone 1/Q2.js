let a = 2;
let b = 3;
let op = "/";
switch(op){
    case "+":
        console.log(a+b);
        break
        case "-":
            console.log(a-b);
            break
            case "*":
                console.log(a*b);
                break;
                case "/":
                    console.log(a/b);
                    break;
                    default:
                        console.log("Invalid Operator");
                        break
}